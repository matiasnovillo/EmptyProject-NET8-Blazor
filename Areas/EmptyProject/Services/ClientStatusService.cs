        #region Importations
        public List<ClientStatus> ImportExcel(string path, int userId)
        {
            List<ClientStatus> lstClientStatus = [];

            var WorkBook = new XLWorkbook(path);
            var Rows = WorkBook.Worksheet(1).RangeUsed().RowsUsed();

            foreach (var row in Rows)
            {
                var rowNumber = row.RowNumber();

                if (rowNumber > 1)
                {
                    string Name = row.Cell(7).GetString();
                    

                    ClientStatus ClientStatus = new()
                    {
                        ClientStatusId = 0,
                        Active = true,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        UserCreationId = userId,
                        UserLastModificationId = userId,
                        Name = Name,
                        
                    };

                    lstClientStatus.Add(ClientStatus);
                }
            }

            return lstClientStatus;
        }
