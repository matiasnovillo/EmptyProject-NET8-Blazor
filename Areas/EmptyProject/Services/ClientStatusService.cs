            DataColumn dtColumnUserCreationIdFordtClientStatusCopy = new()
            {
                DataType = typeof(string),
                ColumnName = "UserCreationId"
            };
            dtClientStatusCopy.Columns.Add(dtColumnUserCreationIdFordtClientStatusCopy);

            DataColumn dtColumnUserLastModificationIdFordtClientStatusCopy = new()
            {
                DataType = typeof(string),
                ColumnName = "UserLastModificationId"
            };
            dtClientStatusCopy.Columns.Add(dtColumnUserLastModificationIdFordtClientStatusCopy);

            DataColumn dtColumnNameFordtClientStatusCopy = new()
            {
                DataType = typeof(string),
                ColumnName = "Name"
            };
            dtClientStatusCopy.Columns.Add(dtColumnNameFordtClientStatusCopy);

            
            #endregion

            dtClientStatusOriginal = dtClientStatus;

            foreach (DataRow DataRow in dtClientStatusOriginal.Rows)
            {
                dtClientStatusCopy.Rows.Add(DataRow.ItemArray);
            }

            var Sheet = Book.Worksheets.Add(dtClientStatusCopy);

            Sheet.ColumnsUsed().AdjustToContents();

            Book.SaveAs(path);
        }

